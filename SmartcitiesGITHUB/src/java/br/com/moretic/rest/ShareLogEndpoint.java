package br.com.moretic.rest;

import java.util.List;

import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.NoResultException;
import javax.persistence.OptimisticLockException;
import javax.persistence.PersistenceContext;
import javax.persistence.TypedQuery;
import javax.ws.rs.*;
import javax.ws.rs.core.Response;
import javax.ws.rs.core.Response.Status;
import javax.ws.rs.core.UriBuilder;
import br.com.moretic.vo.ShareLog;

/**
 * 
 */
@Stateless
@Path("/sharelogs")
public class ShareLogEndpoint
{
   @PersistenceContext(unitName = "smartcitie_db")
   private EntityManager em;

   @POST
   @Consumes("application/json")
   public Response create(ShareLog entity)
   {
      em.persist(entity);
      return Response.created(UriBuilder.fromResource(ShareLogEndpoint.class).path(String.valueOf(entity.getIdshareLog())).build()).build();
   }

   @DELETE
   @Path("/{id:[0-9][0-9]*}")
   public Response deleteById(@PathParam("id") int id)
   {
      ShareLog entity = em.find(ShareLog.class, id);
      if (entity == null)
      {
         return Response.status(Status.NOT_FOUND).build();
      }
      em.remove(entity);
      return Response.noContent().build();
   }

   @GET
   @Path("/{id:[0-9][0-9]*}")
   @Produces("application/json")
   public Response findById(@PathParam("id") int id)
   {
      TypedQuery<ShareLog> findByIdQuery = em.createQuery("SELECT DISTINCT s FROM ShareLog s LEFT JOIN FETCH s.shareViewWith LEFT JOIN FETCH s.operationType LEFT JOIN FETCH s.txnLogs WHERE s.idshareLog = :entityId ORDER BY s.idshareLog", ShareLog.class);
      findByIdQuery.setParameter("entityId", id);
      ShareLog entity;
      try
      {
         entity = findByIdQuery.getSingleResult();
      }
      catch (NoResultException nre)
      {
         entity = null;
      }
      if (entity == null)
      {
         return Response.status(Status.NOT_FOUND).build();
      }
      return Response.ok(entity).build();
   }

   @GET
   @Produces("application/json")
   public List<ShareLog> listAll(@QueryParam("start") Integer startPosition, @QueryParam("max") Integer maxResult)
   {
      TypedQuery<ShareLog> findAllQuery = em.createQuery("SELECT DISTINCT s FROM ShareLog s LEFT JOIN FETCH s.shareViewWith LEFT JOIN FETCH s.operationType LEFT JOIN FETCH s.txnLogs ORDER BY s.idshareLog", ShareLog.class);
      if (startPosition != null)
      {
         findAllQuery.setFirstResult(startPosition);
      }
      if (maxResult != null)
      {
         findAllQuery.setMaxResults(maxResult);
      }
      final List<ShareLog> results = findAllQuery.getResultList();
      return results;
   }

   @PUT
   @Path("/{id:[0-9][0-9]*}")
   @Consumes("application/json")
   public Response update(ShareLog entity)
   {
      try
      {
         entity = em.merge(entity);
      }
      catch (OptimisticLockException e)
      {
         return Response.status(Response.Status.CONFLICT).entity(e.getEntity()).build();
      }

      return Response.noContent().build();
   }
}